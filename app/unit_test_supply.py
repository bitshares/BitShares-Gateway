def unit_test_supply():
    """
    get_objects(1.3.x) # static asset objects
    get_objects(2.3.x) # dynamic asset objects
    """
    rpc = wss_handshake("")

    for network in offerings():
        print(
            rpc_get_objects(rpc, [gateway_assets()[network]["asset_id"]])[0]["symbol"]
        )
        print(
            rpc_get_objects(rpc, [gateway_assets()[network]["dynamic_id"]])[0][
                "current_supply"
            ]
        )
        print("")


if __name__ == "__main__":
    unit_test_supply()
